package com.basics;
public class BitwiseOperators {
    public static void main(String[] args){
        System.out.println(5&6); // AND operator
        System.out.println(5|6); // OR operator
        System.out.println(5^6); // XOR operator
        System.out.println(~5);  // 1's Complement
        System.out.println(6>>1);  // Binary Right shift
        System.out.println(5<<2);  // Binary Left shift '
    }
}
